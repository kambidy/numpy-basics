import numpy as np

np1 = np.array([1,2,3,4,5,6,7,8])
print(np1)

#find square rott of each

print(np.sqrt(np1))

#absolute value

print(np.absolute(np1))

# find exponents
print(np.exp(np1))

# min or max

print(np.min(np1))
print(np.max(np1))

 # find sin,cosine,log

print(np.log(np1))
print(np.cos(np1))
print(np.sin(np1))
